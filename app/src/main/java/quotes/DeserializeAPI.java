package quotes;

public class DeserializeAPI {
     String quoteText;
     String quoteAuthor;
     String senderName;
     String quoteLink;

    public DeserializeAPI(String quoteText, String quoteAuthor, String senderName, String quoteLink) {
        this.quoteText = quoteText;
        this.quoteAuthor = quoteAuthor;
        this.senderName = senderName;
        this.quoteLink = quoteLink;
    }


}
